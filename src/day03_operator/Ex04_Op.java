package day03_operator;

public class Ex04_Op {

	public static void main(String[] args) {
		
		/*
		 [관계 연산자]
		 true(참), false(거짓)
		 a = 10, b = 20;
		 a > b : false
		 a <= : true
		 a == b : false
		 a != b : true
		 */
		
		double do1 = 3.0, do2 = 3.1;
		System.out.println(do1 > do2);
		System.out.println(do1 <= do2);
		System.out.println(do1 == do2);
		System.out.println(do1 != do2);
		

	}

}
